# The input(..) function can be used to read from the terminal (input)

# The input(..) function returns a value, which we can assign to a
# variable, for example.
# For the input(..) function, the function argument (in the brackets)
# is used as a prompt.

# We press enter to confirm the input.
# While we are typing the script is paused.
name = input("Enter your name:")

# print(..) writes to the terminal (output)
print("Hi " + name + "!")